#Operasi Aritmatika

print("\n==========OPERASI ARITMATIKA==========\n")

a = 64
b = 3

#Operasi tambah
hasil = a + b
print(a,'+',b,'=',hasil)

#Operasi pengurangan
hasil = a - b
print(a,'-',b,'=',hasil)

#Operasi perkalian
hasil = a * b
print(a,'x',b,'=',hasil)

#Operasi pembagian
hasil = a / b
print(a,':',b,'=',hasil)

#Operasi eksponen atau perpangkatan
hasil = a ** b
print(a,'**',b,'=',hasil)

#Operasi modulus % sisa dari pembagian
hasil = a % b
print(a,'%',b,'=',hasil)

#Operasi floor division // pembagian double pembulatan
hasil = a // b
print(a,'//',b,'=',hasil)

